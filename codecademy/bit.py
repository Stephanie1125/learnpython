
print(5 >> 4)  # Right Shift = 5 / 16
print(5 << 1)  # Left Shift = 5*2
print(8 & 5)   # Bitwise AND = copies a bit to the result if it exists in both operands = 0
print(9 | 4)  # Bitwise OR = 9+4 = 13
print(12 ^ 42)  # Bitwise XOR = it copies the bit if it is set in one operand but not both.
print(~88)  # Bitwise NOT = -89

print(0b1)    #1
print(0b10)   #2
print(0b11)   #3
print(0b100)  #4
print(0b101)  #5
print(0b110)  #6
print(0b111)   #7
